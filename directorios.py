def buscar_directorios(software):
    if software in common_directories:
        print(f"\nDirectorios comunes de {software.capitalize()}:")
        for directory in common_directories[software]:
            print(directory)
    else:
        print(f"\nSoftware '{software}' desconocido. No se encontraron resultados.")

def guardar_directorios_en_archivo(software, output_file):
    if software in common_directories:
        with open(output_file, 'w') as file:
            file.write(f"Directorios comunes de {software.capitalize()}:\n")
            for directory in common_directories[software]:
                file.write(directory + '\n')
        print(f"Los directorios comunes de {software.capitalize()} se han guardado en '{output_file}'")
    else:
        print(f"Software '{software}' desconocido. No se encontraron resultados.")

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Muestra directorios comunes de diferentes software web.")
    parser.add_argument("software", nargs='?', help="Especifique el nombre del software (p. ej. wordpress, joomla, apache, etc.)")
    parser.add_argument("-o", "--output", help="Especifique el nombre del archivo de salida para guardar los resultados.")

    args = parser.parse_args()
    if args.software:
        if args.output:
            guardar_directorios_en_archivo(args.software, args.output)
        else:
            buscar_directorios(args.software)
    else:
        parser.print_help()
